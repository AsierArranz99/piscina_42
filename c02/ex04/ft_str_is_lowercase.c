int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	alfa;

	alfa = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			alfa++;
		}
		i++;
	}
	if (i != alfa)
	{
		return (0);
	}
	return (1);
}
