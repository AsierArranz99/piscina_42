int	ft_str_is_printable(char *str)
{
	int	i;
	int	alfa;

	alfa = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			alfa++;
		}
		i++;
	}
	if (i != alfa)
	{
		return (0);
	}
	return (1);
}
